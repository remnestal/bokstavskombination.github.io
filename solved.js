function expandSolvedExpandable() {
  document.getElementById("solved-expandable").classList.toggle("fade-in");
  document.getElementById("solved-expandable").classList.toggle("fade-out");
}

function collapseSolvedExpandable() {
  document.getElementById("solved-expandable").classList.toggle("fade-out");
  document.getElementById("solved-expandable").classList.toggle("fade-in");
}