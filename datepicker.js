function openDatepickerExpandable() {
  document.getElementById("datepicker-expandable").classList.toggle("fade-in");
  document.getElementById("datepicker-expandable").classList.toggle("fade-out");
}

function closeDatepickerExpandable() {
  document.getElementById("datepicker-expandable").classList.toggle("fade-out");
  document.getElementById("datepicker-expandable").classList.toggle("fade-in");
}